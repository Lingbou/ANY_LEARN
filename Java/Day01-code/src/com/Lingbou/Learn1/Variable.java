package com.Lingbou.Learn1;

/**
 * Variable Learn
 * 
 * @Lingbou
 * @version 1.0
 */
public class Variable {
    public static void main(String[] args) {
        int a = 666;
        System.out.println(a);
        int b = 344;
        System.out.println(a + b);

        char c = 'F';
        char d = 'u';
        char e = 'c';
        char f = 'k';
        System.out.printf("%s\n", "" + c + d + e +f);

        // Long int
        long long_num = 9999999999999L;
        System.out.println(long_num);

        // float
        float float_num = 3.14F;
        System.out.println(float_num);

        //double
        double double_num = 3.14;
        System.out.println(double_num);
    }
}
