public class Variable {
    public static void main(String[] args) {
        int a = 666;
        System.out.println(a);

        int b = 344;
        System.out.println(a + b);

        char c = 'F';
        char d = 'u';
        char e = 'c';
        char f = 'k';

        System.out.printf("%s\n", "" + c + d + e +f);
    }
}
